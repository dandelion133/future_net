public class Path {
	private int length;
	private String pathStr;
	public int getLength()
	{
		return length;
	}
	public void setLength(int length)
	{
		this.length = length;
	}
	public String getPathStr()
	{
		return pathStr;
	}
	public void setPathStr(String path)
	{
		this.pathStr = path;
	}
}
